INPUT N 
COPY ZERO, COUNT
COPY ZERO, EVENS
LOOP: LOAD COUNT
SUB N
JMPZ END
INPUT NUMBER
LOAD NUMBER
DIV TWO
MUL TWO
SUB NUMBER
JMPZ IS_EVEN
INCREMENT: LOAD COUNT
ADD ONE
STORE CONT
JMP LOOP
IS_EVEN: LOAD EVENS
ADD ONE 
STORE EVENS
JMP INCREMENT
END: OUTPUT EVENS
STOP

N: SPACE
ZERO: CONST 0
COUNT: SPACE
EVENS: SPACE
NUMBER: SPACE
TWO: CONST 2
ONE: CONST 1