MAIN:
	INPUT N
	COPY ONE, COUNT
	COPY ONE, CURRENT_PRIME
	JMP N_PRIME
PRINT:
	OUTPUT CURRENT_PRIME
	STOP
N_PRIME:
	LOAD N
	SUB COUNT
	JMPZ PRINT
	LOAD CURRENT_PRIME
	ADD ONE
	STORE AUX
STOP_AUX:
	COPY TWO, AUX2
VERIFY:
	LOAD AUX
	SUB AUX2
	JMPZ IS_PRIME
	LOAD AUX
	DIV AUX2
	MULT AUX2
	SUB AUX
	JMPZ NOT_PRIME
	LOAD AUX2
	ADD ONE
	STORE AUX2
	JMP VERIFY
IS_PRIME: 
	LOAD COUNT
	ADD ONE
	STORE COUNT
	COPY AUX, CURRENT_PRIME
	JMP N_PRIME
NOT_PRIME:
	LOAD AUX
	ADD ONE
	STORE AUX
	JMP STOP_AUX

N: SPACE
AUX: SPACE
AUX2: SPACE
ONE: CONST 1
TWO: CONST 2
CURRENT_PRIME: SPACE
count:    
space
