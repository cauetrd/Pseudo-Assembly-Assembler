SQUARE: MACRO &A, &B
LOAD &A
MULT &A
STORE &B
ENDMACRO

INPUT N
COPY ZERO, COUNT
COPY ZERO, RESULT
COPY ONE, CURRENT
LOOP:
LOAD N
SUB COUNT
JMPZ FIM
SQUARE CURRENT, AUX ; a fas 
LOAD RESULT
ADD AUX
STORE RESULT
LOAD CURRENT
ADD ONE
STORE CURRENT
LOAD COUNT
ADD ONE 
STORE COUNT
JMP LOOP
FIM: 
OUTPUT RESULT
STOP
N: SPACE
ZERO: CONST 0
COUNT: SPACE
RESULT: SPACE
ONE: CONST 1
CURRENT: SPACE
AUX: SPACE
